public class Metodos{

  public int sumar (int n1 , int n2){
  int resultado = 0;
  resultado = n1 + n2;
  return resultado;
  }
  public int restar (int n1 , int n2){
  int resultado = 0;
  resultado = n1 - n2;
  return resultado;
  }
  public int multiplicar (int n1 , int n2){
  int resultado = 0;
  resultado = n1 * n2;
  return resultado;
  }
  public int dividir (int n1 , int n2){
  int resultado = 0;
  resultado = (int) (n1*1.0/n2);
  return resultado;
  }
}
